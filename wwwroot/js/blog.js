        // Initialize blog on page load
        function initBlog() {
            renderBlogCards();
        }

        // Render blog cards
        function renderBlogCards() {
            const grid = document.getElementById('blogGrid');
            grid.innerHTML = '';

            blogPosts.forEach((post, index) => {
                const card = document.createElement('article');
                card.className = 'blog-card';
                card.onclick = () => openBlogModal(index);
                card.innerHTML = `
                    <div class="blog-card-header">
                        <span class="blog-card-date">${post.date}</span>
                        <span class="blog-card-readtime">${post.readTime}</span>
                    </div>
                    <h3 class="blog-card-title">${post.title}</h3>
                    <p class="blog-card-excerpt">${post.excerpt}</p>
                `;
                grid.appendChild(card);
            });
        }

        // Open blog modal
        function openBlogModal(index) {
            const modal = document.getElementById('blogModal');
            const post = blogPosts[index];

            document.getElementById('modalTitle').textContent = post.title;
            document.getElementById('modalDate').textContent = post.date;
            document.getElementById('modalReadTime').textContent = post.readTime;
            document.getElementById('modalBody').innerHTML = post.content;

            // Display banner image if available
            const bannerContainer = document.getElementById('modalBanner');
            if (post.image) {
                bannerContainer.innerHTML = `<img src="${post.image}" alt="${post.title}">`;
            } else {
                bannerContainer.innerHTML = `<div class="modal-banner-placeholder">${post.title.toUpperCase()}</div>`;
            }

            modal.classList.add('active');
            document.body.style.overflow = 'hidden';
        }

        // Close blog modal
        function closeBlogModal() {
            document.getElementById('blogModal').classList.remove('active');
            document.body.style.overflow = '';
        }

        // Close modal when clicking outside
        document.getElementById('blogModal').addEventListener('click', function (e) {
            if (e.target === this) closeBlogModal();
        });

        // Close modal with Escape key
        document.addEventListener('keydown', function (e) {
            if (e.key === 'Escape') closeBlogModal();
        });

        // Toggle sub-goals expansion
        function toggleSubgoals(element) {
            const expandIcon = element.querySelector('.goal-expand');
            const subgoalsList = element.nextElementSibling;

            // Add spinning animation
            expandIcon.classList.add('spinning');

            // Toggle expanded state
            element.classList.toggle('expanded');
            subgoalsList.classList.toggle('expanded');

            // Remove spinning class after animation completes
            setTimeout(() => {
                expandIcon.classList.remove('spinning');
            }, 400);
        }

